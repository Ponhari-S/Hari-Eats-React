  /**
   * Simulates a Swiggy API fetch.
   * Usage in tests: jest.mock('./dummyAPI') or import and call directly.
   *
   * @param {"restaurants"|"categories"} type
   * @returns {Promise<{statusCode: number, data: object}>}
   */
  export const fetchSwiggyData = (type = "restaurants") => {
    return new Promise((resolve) => {
      setTimeout(() => {
        if (type === "categories") {
          resolve({ statusCode: 0, data: foodCategories });
        } else {
          resolve({ statusCode: 0, data: topRestaurants });
        }
      }, 0); // zero-delay so tests don't need fake timers
    });
  };
  
  // ─── Helper utilities used in filter/search tests ───────────────────────────
  
  /**
   * Filter restaurants by cuisine type.
   * @param {string} cuisine
   * @returns {Array}
   */
  export const filterByCuisine = (cuisine) =>
    topRestaurants.filter((r) =>
      r.cuisines.some((c) => c.toLowerCase() === cuisine.toLowerCase())
    );
  
  /**
   * Filter restaurants by minimum rating.
   * @param {number} minRating
   * @returns {Array}
   */
  export const filterByRating = (minRating) =>
    topRestaurants.filter((r) => r.avgRating >= minRating);
  
  /**
   * Filter only open restaurants.
   * @returns {Array}
   */
  export const getOpenRestaurants = () =>
    topRestaurants.filter((r) => r.isOpen);
  
  /**
   * Filter only veg restaurants.
   * @returns {Array}
   */
  export const getVegRestaurants = () =>
    topRestaurants.filter((r) => r.isVeg);
  
  /**
   * Get a single restaurant by id.
   * @param {string} id
   * @returns {object|undefined}
   */
  export const getRestaurantById = (id) =>
    topRestaurants.find((r) => r.id === id);
  
  /**
   * Sort restaurants by delivery time (fastest first).
   * @returns {Array}
   */
  export const sortByDeliveryTime = () =>
    [...topRestaurants].sort((a, b) => a.deliveryTime - b.deliveryTime);
  
  /**
   * Sort restaurants by rating (highest first).
   * @returns {Array}
   */
  export const sortByRating = () =>
    [...topRestaurants].sort((a, b) => b.avgRating - a.avgRating);
  
  /**
   * Get restaurants currently offering a discount.
   * @returns {Array}
   */
  export const getDiscountedRestaurants = () =>
    topRestaurants.filter((r) => r.discount !== null);
  
